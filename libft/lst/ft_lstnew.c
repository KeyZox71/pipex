#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = malloc(sizeof(t_list));
	if (!lst)
		return (NULL);
	lst[0].content = content;
	lst[0].next = NULL;
	return (lst);
}
